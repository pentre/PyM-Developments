    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String name = branchComboBox.getSelectedItem().toString();
        if (name.equals("")) {
            return;
        }
        
        List<Map<String, String>> results = controller_.searchInventory(name);
        if (results == null || results.size() == 0) {
            JOptionPane.showMessageDialog(this, "Inventario vac√≠o.");
            return;
        }
        
        updateTable(results);
    }//GEN-LAST:event_searchButtonActionPerformed

    private void updateTable(List<Map<String, String>> results) {
        Object[] columnNames = { "ID", "Nombre", "Cantidad" };
        DefaultTableModel model = new DefaultTableModel(columnNames,0);
        
        for(int i=0; i<results.size(); i++) {
            Object[] row = {
                results.get(i).get("id"),
                results.get(i).get("name"),
                results.get(i).get("quantity"),              
            };
            model.addRow(row);
        }
        inventoryTable.setModel(model);
    }
    
